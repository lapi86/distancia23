/**
 * Función para sumar dos números enteros
 *
 * @param int $a Primer número entero
 * @param int $b Segundo número entero
 * @return int Resultado de la suma
 * @since 1.0
 */
function sumar($a, $b) {
  return $a + $b;
}

/**
 * Función para multiplicar dos números enteros
 *
 * @param int $a Primer número entero
 * @param int $b Segundo número entero
 * @return int Resultado de la multiplicación
 * @since 1.0
 */
function multiplicar($a, $b) {
  return $a * $b;
}

